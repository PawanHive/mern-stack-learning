/*
************** Node-REPL **************

1. What is Node-REPL?
REPL = Read –> Evaluate –> Print –> Loop

That’s exactly what Node does:

    1. Read → reads your input
    2. Evaluate → executes the JavaScript
    3. Print → shows the result
    4. Loop → waits for next input

It’s an interactive Node.js environment where you can run 
JavaScript line by line without creating a file.

------------------------------------------------------------------

2. How to Start REPL:

Open terminal / command prompt and type: node

-------------------------------------------------------------------

3. When Should You Use REPL?

Use REPL when you want to:

Test JS logic
Check Node APIs
Debug small code
Learn new methods

❌ Don’t use it for big apps.

------------------------------------------------------------------

4. REPL Commands (VERY IMPORTANT):
==> These start with a dot(.)

node = start node-REPL

.exit = exit REPL

ctrl + C (twice) = exit REPL

.clear = clear REPL

.help = shows all REPL commands.

.load index.js = Load a File into REPL (runs file line by line inside REPL.)

.save repl.js = save REPL session (saves everything we typed)

*/